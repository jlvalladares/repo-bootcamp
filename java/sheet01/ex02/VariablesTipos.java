public class VariablesTipos {
    public static void main(String[] args){
        int numeroEntero = 42;
        double pI = 3.1416;
        boolean laVerdad = true;
        char a = 'A';
        String cadenaTexto = "¡Hola Mundo!";

    //Impresion de valores por la patilla
        System.out.println("Número entero: " + numeroEntero);
        System.out.println("Número decimal: " + pI);
        System.out.println("Valor lógico: " + laVerdad);
        System.out.println("Caracter: " + a);
        System.out.println("Cadena de texto: " + cadenaTexto);
    }
} 